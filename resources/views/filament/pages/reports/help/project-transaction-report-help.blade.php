                <li><strong>Rental</strong>: Revenue from property rentals.</li>
                <li><strong>Investment</strong>: Revenue from investment returns.</li>
                <li><strong>Other</strong>: Other types of revenue.</li>
            </ul>
        </div>

        <div>
