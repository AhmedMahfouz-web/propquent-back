        <div>
            <h4 class="font-medium text-gray-800">Report Overview</h4>
            <p>The Project Transaction Report tracks project revenue and expenses over time, categorizes transactions,
                and calculates net cash flow for each project.</p>
        </div>

        <div>
            <h4 class="font-medium text-gray-800">Filters</h4>
            <ul class="list-disc pl-5 space-y-1">
                <li><strong>From Date / To Date</strong>: Select the date range for the report.</li>
                <li><strong>Project</strong>: Filter the report for a specific project or view data for all projects.
                </li>
            </ul>
        </div>

        <div>
            <h4 class="font-medium text-gray-800">Summary Metrics</h4>
            <ul class="list-disc pl-5 space-y-1">
                <li><strong>Total Revenue</strong>: The sum of all revenue transactions within the selected date range.
                </li>
                <li><strong>Total Expenses</strong>: The sum of all expense transactions within the selected date range.
                </li>
                <li><strong>Net Cash Flow</strong>: The difference between total revenue and total expenses.</li>
            </ul>
        </div>

        <div>
            <h4 class="font-medium text-gray-800">Monthly Data Table</h4>
            <p>This table shows the monthly breakdown of revenue, expenses, and net cash flow over time.</p>
            <ul class="list-disc pl-5 space-y-1">
                <li><strong>Month</strong>: The month and year of the transactions.</li>
                <li><strong>Total Revenue</strong>: The sum of all revenue transactions for the month.</li>
                <li><strong>Total Expenses</strong>: The sum of all expense transactions for the month.</li>
                <li><strong>Net Cash Flow</strong>: The difference between revenue and expenses for the month.</li>
            </ul>
        </div>

        <div>
            <h4 class="font-medium text-gray-800">Revenue by Category</h4>
